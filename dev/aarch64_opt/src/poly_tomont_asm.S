        mls v0.8H, v28.8H, v4.H[0]         // *.............................
        str q26, [x0, #-16]                // *.............................
        mls v18.8H, v24.8H, v4.H[0]        // ..*...........................
        str q0, [x0, #-48]                 // .....*........................
        str q18, [x0, #-32]                // .......*......................

