        // mul      v1.8h, v0.8h, v2.8h           // ..........*.............'.......
        // mls      v1.8h, v6.8h,    v4.h[0]      // ..............*.........'.......
        // str q1, [x0, #-64]                     // ...................*....'.......
        // ldr q0, [x0, #-48]                     // *.......................~.......
        // sqrdmulh v6.8h,    v0.8h, v3.8h        // ....*...................'...~...
        // mul      v1.8h, v0.8h, v2.8h           // ......*.................'.....~.
        // mls      v1.8h, v6.8h,    v4.h[0]      // ~.......................l.......
        // str q1, [x0, #-48]                     // .....~..................'....l..
        // ldr q0, [x0, #-32]                     // ..*.....................'.~.....
        // sqrdmulh v6.8h,    v0.8h, v3.8h        // ..................*.....'.......
        // mul      v1.8h, v0.8h, v2.8h           // ................*.......'.......
        // mls      v1.8h, v6.8h,    v4.h[0]      // ..~.....................'.l.....
