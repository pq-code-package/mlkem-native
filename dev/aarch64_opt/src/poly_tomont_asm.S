/* Montgomery multiplication, with precomputed Montgomery twist
 * Expects modulus in consts.h[0]. */
.macro mulmod dst, src, const, const_twisted
        sqrdmulh tmp0.8h,    \src\().8h, \const_twisted\().8h
        mul      \dst\().8h, \src\().8h, \const\().8h
        mls      \dst\().8h, tmp0.8h,    modulus.h[0]
.endm

/**********************************
 *      mlk_poly_tomont()         *
 **********************************/

        src               .req x0
        count             .req x1
        wtmp              .req w2

        data              .req v0
        q_data            .req q0
        res               .req v1
        q_res             .req q1

        factor            .req v2
        factor_t          .req v3
        modulus           .req v4
        modulus_twisted   .req v5

        tmp0              .req v6

        .text
        .global MLK_ASM_NAMESPACE(poly_tomont_asm)
        .balign 4
MLK_ASM_FN_SYMBOL(poly_tomont_asm)

        mov wtmp, #3329 // ML-KEM modulus
        dup modulus.8h, wtmp

        mov wtmp, #20159 // Barrett twist of 1 wrt 2^27
        dup modulus_twisted.8h, wtmp

        mov wtmp, #-1044 // 2^16 % 3329
        dup factor.8h, wtmp

        mov wtmp, #-10276 // Barrett twist of -1044 (wrt 2^16)
        dup factor_t.8h, wtmp

        mov count, #8
                                              // Instructions:    15
                                              // Expected cycles: 23
                                              // Expected IPC:    0.65
                                              //
                                              // Cycle bound:     23.0
                                              // IPC bound:       0.65
                                              //
                                              // Wall time:     0.10s
                                              // User time:     0.10s
                                              //
                                              // ----- cycle (expected) ------>
                                              // 0                        25
                                              // |------------------------|----
        ldr q28, [x0, #48]                    // *.............................
        ldr q26, [x0], #64                    // .*............................
        ldr q17, [x0, #-32]                   // ..*...........................
        sqrdmulh v16.8H, v28.8H, v3.8H        // ....*.........................
        sqrdmulh v24.8H, v17.8H, v3.8H        // ......*.......................
        mul v18.8H, v26.8H, v2.8H             // ........*.....................
        sqrdmulh v22.8H, v26.8H, v3.8H        // ..........*...................
        mul v26.8H, v28.8H, v2.8H             // ............*.................
        ldr q28, [x0, #-48]                   // ............*.................
        mls v26.8H, v16.8H, v4.H[0]           // ..............*...............
        mls v18.8H, v22.8H, v4.H[0]           // ................*.............
        mul v0.8H, v28.8H, v2.8H              // ..................*...........
        sqrdmulh v28.8H, v28.8H, v3.8H        // ....................*.........
        str q18, [x0, #-64]                   // .....................*........
        mul v18.8H, v17.8H, v2.8H             // ......................*.......

                                               // ------ cycle (expected) ------>
                                               // 0                        25
                                               // |------------------------|-----
        // ldr q19, [x0, #16]                  // ............*..................
        // ldr q16, [x0, #48]                  // *..............................
        // ldr q23, [x0, #32]                  // ..*............................
        // ldr q17, [x0], #64                  // .*.............................
        // sqrdmulh v28.8H, v19.8H, v3.8H      // ....................*..........
        // mul v0.8H, v19.8H, v2.8H            // ..................*............
        // sqrdmulh v22.8H, v17.8H, v3.8H      // ..........*....................
        // mul v26.8H, v17.8H, v2.8H           // ........*......................
        // sqrdmulh v29.8H, v16.8H, v3.8H      // ....*..........................
        // mls v26.8H, v22.8H, v4.H[0]         // ................*..............
        // mul v18.8H, v23.8H, v2.8H           // ......................*........
        // sqrdmulh v24.8H, v23.8H, v3.8H      // ......*........................
        // str q26, [x0, #-64]                 // .....................*.........
        // mul v26.8H, v16.8H, v2.8H           // ............*..................
        // mls v26.8H, v29.8H, v4.H[0]         // ..............*................

        sub count, count, #1
poly_tomont_loop:
                                              // Instructions:    20
                                              // Expected cycles: 24
                                              // Expected IPC:    0.83
