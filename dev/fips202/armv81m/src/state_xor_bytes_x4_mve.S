@ ----------------------------------------------------------------------------
@ uint32x4_t keccak_f1600_x4_state_xor_bytes_aligned(uint32_t nvecs, uint8_t* state, uint32x4_t data_ptrs)
@ WARNING: Assumes that length limits are already applied
@ WARNING: Assumes that state is offset by -16
@----------------------------------------------------------------------------

.balign 8
.global MLK_ASM_NAMESPACE(keccak_f1600_x4_state_xor_bytes_aligned)
.type MLK_ASM_NAMESPACE(keccak_f1600_x4_state_xor_bytes_aligned), %function
MLK_ASM_FN_SYMBOL(keccak_f1600_x4_state_xor_bytes_aligned)
    push {r4, lr}
    vpush {d8 - d15}
    @ Setup the two state pointers
    add r2, r1, #400    @ r2 = state high, r1 = state low
    @ Offset the data pointers
    @ Note: q4 used instead of q7 because local subroutine clobbers q7
    mov r3, #4
    vsub.u32 q4, q0, r3
    @ xor each vector into the state
    wls lr, r0, keccak_f1600_x4_state_xor_bytes_aligned_loop_end
keccak_f1600_x4_state_xor_bytes_aligned_loop_start:
    @ load each vector (8 bytes from each of 4 pointers)
    vldrw.u32 q0, [q4, #4]!
    vldrw.u32 q1, [q4, #4]!
    @ Convert to bit-interleaved representation
    mov r4, lr
    bl keccak_f1600_x4_state_xor_bytes_aligned_to_bit_interleaving
    mov lr, r4
    @ q0 = even bits, q1 = odd bits
    @ load state
    vldrw.u32 q2, [r1, #16]
    vldrw.u32 q3, [r2, #16]
    @ xor bit interleaved vector with state
    veor      q2, q2, q0
    veor      q3, q3, q1
    @ write state back
    vstrw.u32 q2, [r1, #16]!
    vstrw.u32 q3, [r2, #16]!
    @ decrement length
    le  lr, keccak_f1600_x4_state_xor_bytes_aligned_loop_start
keccak_f1600_x4_state_xor_bytes_aligned_loop_end:
    vadd.u32 q0, q4, r3
    vpop {d8 - d15}
    pop {r4, pc}

